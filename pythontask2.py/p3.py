# Declaring a variable
x = 10

# Using assignment operators
print("Initial value of x:", x)  # Display the initial value

x += 5   # Equivalent to: x = x + 5
print("After x += 5:", x)

x -= 3   # Equivalent to: x = x - 3
print("After x -= 3:", x)

x *= 2   # Equivalent to: x = x * 2
print("After x *= 2:", x)

x /= 4   # Equivalent to: x = x / 4
print("After x /= 4:", x)

x %= 3   # Equivalent to: x = x % 3
print("After x %= 3:", x)

x **= 2  # Equivalent to: x = x ** 2
print("After x **= 2:", x)

x //= 2  # Equivalent to: x = x // 2
print("After x //= 2:", x)
