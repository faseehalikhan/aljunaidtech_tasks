#Declaring variable with different data types
integer_var = 25 #integer
float_var = 10.5 #float (decimal number)
string_var = "Hello" #string(text)
bool_var = True #boolean(True/False)
list_var =[1,2,3] #list (collection of items)
tuple_var = (4,5,6) # tuple (immutable collection)
dict_var = {"name": "Faseeh","age": 24} #dictionary (key-value pairs)
set_var = {7,8,9} #set(unordered unique items)
complex_var = 3+4j # complex number

#printing numbers along with their data types
print("integer_var:", integer_var,"Type:", type(integer_var))
print("float_var:", float_var, "type:", type(float_var))
print("string_var:", string_var,"type:",type(string_var))
print("bool_var:", bool_var, "Type:", type(bool_var))
print("list_var:",list_var, "type:", type(list_var))
print("tuple_var:", tuple_var, "Type:", type(tuple_var))
print("dict_var:", dict_var, "Type:", type(dict_var))
print("set_var:", set_var, "type:", type(set_var))
print("complex_var:", complex_var, "Type:", type(complex_var))